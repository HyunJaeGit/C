					// stdio.h : 표준 입출력 헤더. printf()가 여기 있음

int main() {		// main : 프로그램에서 딱 하나만 존재. 여기서 부터 코드가 실행

	printf("Hello~");	// printf() : 표준 출력 함수. 데이터를 화면에 출력
						// "" (쌍따옴표) : 문자열을 의미

}	// {} : 종속문장 범위를 표현